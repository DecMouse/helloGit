def sumNumbers(a, b):
    return a + b

def multiplyNumbers(a, b):
    return a * b

def subtractNumbers(a, b):
    return a - b

def divideNumbers(a, b):
    if b == 0:
        return "Cannot divide by zero"
    return a / b   
        
def powerNumbers(a, b):
    return a ** b

def moduloNumbers(a, b):
    return a % b

print("Sum:", sumNumbers(5, 3))
